<p>For continued display, client browsers should ensure:</p>
<ol>
<li>Ensure OS task scheduler will not start anything</li>
<li>Ensure virus checker does not autmoatically start a scan</li>
<li>Ensure OS does not schedule an update</li>
<li>Disable power management plans (PC always on)</li>
<li>Disable screen saver</li>
<li>Switch off all IM clients</li>
<li>Ensure browser does not refresh web pages from its local cache</li>
<li>Press <kbd>F11</kbd> to put browser in full screen mode</li>
</ol>
<p>It's better to switch off the built-in screen when using an external screen on an HDMI connection. This allows the graphics card to adjust to the resolution of the external screen.</p>
