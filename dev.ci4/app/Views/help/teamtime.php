<p>For continued display, client browsers should ensure:</p>
<ul>
<li><abbr title="operating system">OS</abbr> task scheduler will not start anything</li>
<li>virus checker does not automatically start a scan</li>
<li><abbr title="operating system">OS</abbr> does not schedule an update</li>
<li>power management plans are disabled (PC always on)</li>
<li>screen saver is disabled</li>
<li>all <abbr title="instant messaging">IM</abbr> clients are off</li>
<li>browser does not load web pages from its local cache</li>
<li>browser is in full screen mode (<kbd>F11</kbd>)</li>
</ul>
<p>It's better to switch off the built-in screen when using an external screen on an HDMI connection. This allows the graphics card to adjust to the resolution of the external screen.</p>
