<h3>Getting logged in</h3>
<p>Be careful to enter the address of the console exactly as shown, especially when using devices like smart-phones. Depending on the server in use, there may not be <code>www</code> at the start of the address, and you may need <code>:3000</code> at the end.</p>
<p>You will need a user name and password to access the console. Please ask Kevin for it.</p>
<p>If things don't work, or you can't log-in:</p>
<ul>
	<li>try re-loading the page (<kbd>[Ctrl] + R</kbd>)</li>
	<li>try clearing your browser cache</li>
</ul>

<h3>Instructions</h3>
<p><a href="/app/scoreboard/instructions.pdf" target="_blank">You can down-load instructions here</a>.</p>
<p>You may need to write scores down for coaches to see as well. We suggest using a blank manual score sheet and recording all scores for each rotation on one piece of paper. This can be sent to control desk at the end of the rotation to act as a double check on the scores you have entered.</p>

<h3>On the day</h3>
<p>The web address of these links will be different on the day.</p>
<p>The log-in details given out before the day will not work at the event. Each judge panel will be required to create a username / password, and use this for event.</p> 
<p>Scores can be entered using any internet enabled device (lap-top, smart phone, tablet, etc). We will not supply a device to enter scores; each judge panel needs to supply their own lap-top or similar (NB: each judge panel has one device shared between all the judges). It's easier if one judge from each judge panel performs all the data-entry for that panel.</p>
<p>Please ensure the device can run on batteries for the whole session. We will try, but there is no guarantee we can get power to every judge panel.</p>
<p>All data entered in to the judge console before the day will be erased on the day.</p>