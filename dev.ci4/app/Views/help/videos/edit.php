<p>Please make an effort to share videos from your preferred video host rather than upload them to our server. Videos can take up a massive amount of space, which Google, etc can accommodate far better than we can.</p>

<h3>Examples of how to share</h3>
<h4>Google photos:</h4>
<ol>
<li>Browse to the video you wish to use</li>
<li>Click the "share" button (top right)</li>
<li>Click "create link" (note that anyone with the link will be able to view the video)</li>
<li>Copy the generated link (something like https://photos.app.goo.gl/abc)</li>
<li>Browse back to the page containing the videos for you gymnast and paste the into the correct exercise</li>
</ol>
