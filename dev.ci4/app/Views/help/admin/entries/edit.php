<h4>Run order</h4>
<p>All values are integers</p>
<dl>
<dt>rnd</dt><dd>the round this gymnast appears in</dd>
<dt>rot</dt><dd>the rotation the gymnast performs on their first piece</dd>
<dt>exe</dt><dd>the number of their first piece (taken from the category's exercise set).</dd>
</dl>
<p>Most of the time, the start rotation will be "1". Gymnasts will only start on later rotation in  large (complicated) events.</p> 